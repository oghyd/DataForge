import { z } from "zod";

export const matchCreateSchema = z.object({
  homeTeamId: z.string().min(1, "Home team is required"),
  awayTeamId: z.string().min(1, "Away team is required"),
  homeClubId: z.string().optional().nullable(),
  awayClubId: z.string().optional().nullable(),
  matchDate: z.string().min(1, "Match date is required"),
  kickoffTime: z.string().optional().nullable(),
  seasonId: z.string().optional().nullable(),
  competitionId: z.string().optional().nullable(),
  venueId: z.string().optional().nullable(),
  matchDay: z.coerce.number().int().min(1).optional().nullable(),
  stage: z.string().optional().nullable(),
  status: z.enum(["SCHEDULED", "IN_PROGRESS", "COMPLETED", "POSTPONED", "CANCELLED", "ABANDONED"]).default("SCHEDULED"),
  homeScore: z.coerce.number().int().min(0).optional().nullable(),
  awayScore: z.coerce.number().int().min(0).optional().nullable(),
  homeHalfTimeScore: z.coerce.number().int().min(0).optional().nullable(),
  awayHalfTimeScore: z.coerce.number().int().min(0).optional().nullable(),
  homePenaltyScore: z.coerce.number().int().min(0).optional().nullable(),
  awayPenaltyScore: z.coerce.number().int().min(0).optional().nullable(),
  weather: z.string().optional().nullable(),
  pitchCondition: z.string().optional().nullable(),
  attendance: z.coerce.number().int().min(0).optional().nullable(),
  refereeName: z.string().optional().nullable(),
  notes: z.string().max(5000).optional().nullable(),
});

