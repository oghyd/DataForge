export const matchEventSchema = z.object({
  matchId: z.string().min(1),
  playerId: z.string().optional().nullable(),
  type: z.enum([
    "GOAL", "OWN_GOAL", "ASSIST", "YELLOW_CARD", "RED_CARD", "SECOND_YELLOW",
    "SUBSTITUTION_IN", "SUBSTITUTION_OUT", "PENALTY_SCORED", "PENALTY_MISSED",
    "INJURY", "VAR_DECISION",
  ]),
  minute: z.coerce.number().int().min(0).max(150),
  addedTime: z.coerce.number().int().min(0).optional().nullable(),
  half: z.enum(["FIRST_HALF", "SECOND_HALF", "EXTRA_TIME_1", "EXTRA_TIME_2", "PENALTY_SHOOTOUT"]).optional().nullable(),
  teamSide: z.enum(["HOME", "AWAY"]).optional().nullable(),
  description: z.string().max(500).optional().nullable(),
  relatedPlayerId: z.string().optional().nullable(),
});

export type MatchCreateInput = z.infer<typeof matchCreateSchema>;
export type TeamMatchStatsInput = z.infer<typeof teamMatchStatsSchema>;
export type PlayerMatchStatsInput = z.infer<typeof playerMatchStatsSchema>;
export type MatchEventInput = z.infer<typeof matchEventSchema>;
